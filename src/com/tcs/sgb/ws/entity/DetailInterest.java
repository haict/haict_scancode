package com.tcs.sgb.ws.entity;

public class DetailInterest {
	private String dateInterest;
	private String firstBal;
	private String rateAcct;
	private String interestAcct;
	private String typeBalAcct;
	private String narrative;

	public String getDateInterest() {
		return dateInterest;
	}

	public void setDateInterest(String dateInterest) {
		this.dateInterest = dateInterest;
	}

	public String getFirstBal() {
		return firstBal;
	}
	public void setFirstBal(String firstBal) {
		this.firstBal = firstBal;
	}

	public String getRateAcct() {
		return rateAcct;
	}

	public void setRateAcct(String rateAcct) {
		this.rateAcct = rateAcct;
	}

	public String getInterestAcct() {
		return interestAcct;
	}

	public void setInterestAcct(String interestAcct) {
		this.interestAcct = interestAcct;
	}

	public String getTypeBalAcct() {
		return typeBalAcct;
	}

	public void setTypeBalAcct(String typeBalAcct) {
		this.typeBalAcct = typeBalAcct;
	}

	public String getNarrative() {
		return narrative;
	}

	public void setNarrative(String narrative) {
		this.narrative = narrative;
	}

}
